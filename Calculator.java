package assignment1.week1;

public class Calculator {

	public int addNum(int a, int b, int c)
	{
		int sum =a+b+c;
		return(sum);
	}
	public int subNum(int a,int b)
	{
		int sub= a-b;
		return(sub);
	}
	public double mulNum(double a,double b)
	{
		double mul=a*b;
		return(mul);
	}
	public float divNum(float a,float b)
	{
		float div=a/b;
		return(div);
	}

}
